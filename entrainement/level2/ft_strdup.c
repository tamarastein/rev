char	*ft_strdup(const char *s1)
{
	int		i;
	char *dest;

	dest = malloc(sizeof(s1));
	i = 0;
	while (s1[i] != 0)
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
}
